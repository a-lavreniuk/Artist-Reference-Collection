/** Sanity: copy lists stay aligned with prefs ids. */
export function assertJoyCaptionCopyComplete(): boolean {
  return (
    JOY_CAPTION_TYPE_OPTIONS.length === JOY_CAPTION_TYPE_IDS.length &&
    JOY_CAPTION_EXTRA_OPTIONS.length === JOY_CAPTION_EXTRA_IDS.length
  );
}

export function captionLengthLabel(level: number): string {
  if (level <= 0) return 'Без ограничения';
  if (level <= 20) return 'Очень коротко';
  if (level <= 40) return 'Коротко';
  if (level <= 60) return 'Средняя длина';
  if (level <= 80) return 'Длинное';
  return 'Очень длинное';
}

export function captionLengthHint(level: number): string {
  if (level <= 0) return 'Без ограничения длины — модель сама выбирает объём';
  if (level <= 20) return 'Очень коротко — пара фраз или минимум деталей';
  if (level <= 40) return 'Коротко — сжатое описание без лишней воды';
  if (level <= 60) return 'Средняя длина — баланс деталей и читаемости';
  if (level <= 80) return 'Длинное — развёрнутое описание с деталями';
  return 'Очень длинное — максимум подробностей';
}
